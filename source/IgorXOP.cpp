int ExecuteSOFIAnalysis(SOFIAnalysisRuntimeParamsPtr p) {
    int err = 0;

	RecycledMatrixCleaner recycledMatrixCleaner;

    // Flag parameters.
	
    int cameraType;
    if (p->YFlagEncountered) {
        // Parameter: p->cameraType
		if (p->cameraType < 0) {
			cameraType = -1;
		} else {
			cameraType = (int)(p->cameraType + 0.5);
		}
    } else {
        cameraType = -1;
    }

    double psfWidth;
    if (p->WDTHFlagEncountered) {
        // Parameter: p->psfWidth
        if (p->psfWidth <= 0)
            return EXPECT_POS_NUM;
        psfWidth = p->psfWidth;
    } else {
        psfWidth = 1.6;
    }

    int order;
    if (p->ORDRFlagEncountered) {
        // Parameter: p->order
        order = static_cast<int>(p->order + 0.5);
        if (order <= 0)
            return EXPECT_POS_NUM;
    } else {
        order = 2;
    }
	
	std::vector<int> lagTimes;
	// only one of the LAG or LAGW flags can be specified
	if (p->LAGFlagEncountered && p->LAGWFlagEncountered) {
		XOPNotice("Only one of the \"LAG\" or \"LAGW\" flags can be set\r");
		return PALM_ANALYSIS_XOP_ERROR;
	}
	
    if (p->LAGFlagEncountered) {
		// Array-style optional parameter: p->lagTimes
		int* paramsSet = &p->LAGFlagParamsSet[0];
		int nLagTimesSpecified = 0;
		double d1;
		for(int i=0; i<100; i++) {
			if (paramsSet[i] == 0)
				break;		// No more parameters.
			d1 = p->lagTimes[i];
			if (d1 < 0)
				return EXPECT_POS_NUM;
			int lagTime = static_cast<int>(d1 + 0.5);
			lagTimes.push_back(lagTime);
			nLagTimesSpecified += 1;
		}
		if (nLagTimesSpecified != order - 1) {
			XOPNotice("SOFI calculation of order n requires specification of exactly (n - 1) lag times\r");
			return PALM_ANALYSIS_XOP_ERROR;
		}
	}
	
	if (p->LAGWFlagEncountered) {
		// Parameter: p->lagTimesWave (test for NULL handle before using)
		waveHndl lagTimesWave = p->lagTimesWave;
		if (lagTimesWave == NULL)
			return NOWAV;
		int waveType = WaveType(lagTimesWave);
		if ((waveType == TEXT_WAVE_TYPE) || (waveType == WAVE_TYPE) || (waveType == DATAFOLDER_TYPE) || (waveType & NT_CMPLX))
			return NT_INCOMPATIBLE;
		int nLagTimesInWave = WavePoints(lagTimesWave);
		if (nLagTimesInWave < order - 1) {
			XOPNotice("SOFI calculation of order n requires specification of exactly (n - 1) lag times\r");
			return PALM_ANALYSIS_XOP_ERROR;
		}
		std::unique_ptr<double[]> doubleBuffer(new double[nLagTimesInWave]);
		lagTimes.resize(nLagTimesInWave);
		err = MDGetDPDataFromNumericWave(lagTimesWave, doubleBuffer.get());
		if (err != 0)
			return err;
		for (int i = 0; i < nLagTimesInWave; ++i) {
			lagTimes[i] = doubleBuffer[i];
		}
		// drop any lag times that may be unnecessary
		lagTimes.resize(order - 1);
	}

    int crossCorrelate;
    if (p->XCFlagEncountered) {
        // Parameter: p->doCrossCorrelation
        if ((int)(p->doCrossCorrelation + 0.5) == 0) {
            crossCorrelate = 0;
        } else {
            crossCorrelate = 1;
        }
    } else {
        crossCorrelate = 0;
    }

    int nFramesToGroup;
    if (p->GRPFlagEncountered) {
        // Parameter: p->nFramesToGroup
        if (p->nFramesToGroup < 0)
            return EXPECT_POS_NUM;
        nFramesToGroup = (int)(p->nFramesToGroup + 0.5);
    } else {
        nFramesToGroup = 0;
    }

    double maxPixelVal = 0.0;
    int limitMaxPixelVal = 0;
    if (p->MAXFlagEncountered) {
        // Parameter: p->maxPixelVal
        maxPixelVal = p->maxPixelVal;
        limitMaxPixelVal = 1;
    }

    int noSaturatedPixels = 0;
    if (p->NSATFlagEncountered) {
        noSaturatedPixels = 1;
        if (p->NSATFlagParamsSet[0]) {
            // Optional parameter: p->noSaturatedPixels
            noSaturatedPixels = (int)(p->noSaturatedPixels + 0.5);
        }
    }
    
    int doAverage = 0;
    if (p->AVGFlagEncountered) {
        doAverage = 1;
		if (p->AVGFlagParamsSet[0]) {
			// Optional parameter: p->doAverage
            doAverage = static_cast<int>(p->doAverage + 1);
		}
	}

    int nFramesToSkip = 0;
    int nFramesToInclude = -1;
    if (p->SUBFlagEncountered) {
		// Parameter: p->framesToSkip
        if (p->framesToSkip > 0)
            nFramesToSkip = (int)(p->framesToSkip + 0.5);
		// Parameter: p->nFramesToInclude
        if (p->nFramesToInclude > 0)
            nFramesToInclude = (int)(p->nFramesToInclude + 0.5);
	}

    int useIgorFunctionForProgress;
    FUNCREF igorProgressReporterFunction;
    if (p->PROGFlagEncountered) {
        // Parameter: p->progStruct
        useIgorFunctionForProgress = 1;
        igorProgressReporterFunction = p->progStruct->funcRef;
    } else {
        useIgorFunctionForProgress = 0;
    }

    int quiet = 0;
    if (p->QFlagEncountered) {
        quiet = 1;
    } else {
        if (RunningInMainThread() != 1)
            quiet = 1;	// no progress reporting if running in an Igor-preemptive thread
    }

    DataFolderAndName outputWaveParams;
    if (p->DESTFlagEncountered) {
        // Parameter: p->dest
        outputWaveParams = p->dest;
    } else {
        outputWaveParams.dfH = NULL;
        strcpy(outputWaveParams.name, "M_SOFI");
    }

    // Main parameters.
    if (p->inputFilePathEncountered) {
        // Parameter: p->inputFilePath (test for NULL handle before using)
        if (p->inputFilePath == NULL)
            return EXPECTED_STRING_EXPR;
    } else {
        return EXPECTED_STRING_EXPR;
    }

    try {
        std::string inputFilePath = ConvertHandleToString(p->inputFilePath);
        std::shared_ptr<ImageLoader> imageLoader = GetImageLoader(inputFilePath, cameraType);

        if (nFramesToSkip >= imageLoader->getNImages())
            return ORN_OFFSET_OUT_OT_RANGE;
        int nGroups;
        size_t nImagesToProcess;
        if (nFramesToInclude == -1) {
            nImagesToProcess = imageLoader->getNImages() - nFramesToSkip;
        } else {
            nImagesToProcess = nFramesToInclude;
        }

        if (nFramesToGroup != 0) {
            nGroups = ceil((double)(nImagesToProcess) / (double)(nFramesToGroup));
        } else {
            nGroups = 1;
        }

        // check if any frame verifiers need to be used
        std::shared_ptr<SOFIFrameVerifier> sofiFrameVerifier;
        std::vector<std::shared_ptr<SOFIFrameVerifier> > frameVerifiers;

        if (noSaturatedPixels != 0) {
            sofiFrameVerifier = std::shared_ptr<SOFIFrameVerifier> (new SOFIFrameVerifier_NoSaturation(imageLoader->getStorageType()));
            frameVerifiers.push_back(sofiFrameVerifier);
        }
        if (limitMaxPixelVal != 0) {
            sofiFrameVerifier = std::shared_ptr<SOFIFrameVerifier> (new SOFIFrameVerifier_MaxPixelValue(maxPixelVal));
            frameVerifiers.push_back(sofiFrameVerifier);
        }

        std::shared_ptr<ImageOutputWriter> outputWriter(new IgorImageOutputWriter(outputWaveParams, nGroups, 1, kFP64));
        std::shared_ptr<ImageOutputWriter> averageOutputWriter;
        DataFolderAndName averageOutputWaveParams;
        
        if (doAverage != 0) {
            // check that the requested wave name is not too long for the output name
            // return an error otherwise
            if (strlen(outputWaveParams.name) > MAX_OBJ_NAME - 4)   // make room for the "_avg" suffix
                return NAME_TOO_LONG;
            averageOutputWaveParams.dfH = outputWaveParams.dfH;
            strcpy(averageOutputWaveParams.name, outputWaveParams.name);
            strcat(averageOutputWaveParams.name, "_avg");
            averageOutputWriter = std::shared_ptr<ImageOutputWriter>(new IgorImageOutputWriter(averageOutputWaveParams, nGroups, 1, kFP64));
        }

        std::shared_ptr<ProgressReporter> progressReporter;

        if (quiet == 1) {
            progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_Silent);
        } else {
            if (useIgorFunctionForProgress != 0) {
                progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_IgorUserFunction(igorProgressReporterFunction));
            } else {
                progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_IgorCommandLine);
            }
        }
		
		std::vector<ImagePtr> sofiImages, averageImages;
        DoSOFIAnalysis(imageLoader, frameVerifiers, progressReporter, nFramesToSkip, nFramesToInclude, lagTimes, order, crossCorrelate, nFramesToGroup, sofiImages, averageImages);
		for (size_t i = 0; i < sofiImages.size(); ++i) {
			outputWriter->write_image(sofiImages.at(i));
			if (doAverage != 0)
				averageOutputWriter->write_image(averageImages.at(i));
		}
    }
    catch (int e) {
        return e;
    }
    catch (std::bad_alloc e) {
        return NOMEM;
    }
    catch (std::runtime_error e) {
        XOPNotice(e.what());
        XOPNotice("\r");
        return PALM_ANALYSIS_XOP_ERROR;
    }
    catch (...) {
        XOPNotice("An unknown error occurred\r");
        return WM_UNKNOWN_ERROR;
    }

    return err;
}

static int ExecuteNewSOFI(NewSOFIRuntimeParamsPtr p) {
	int err = 0;

	RecycledMatrixCleaner recycledMatrixCleaner;
    
    int cameraType = -1;
    if (p->YFlagEncountered) {
		// Parameter: p->cameraType
        cameraType = p->cameraType + 0.5;
	}
    
    std::vector<int> orders;
    if (p->ORDRFlagEncountered) {
		// Parameter: p->order
        int order = static_cast<int>(p->order);
        if (order < 1) {
            return EXPECT_POS_NUM;
        }
        orders.push_back(order);
        
        // Array-style optional parameter: p->extraOrders
		int* paramsSet = &p->ORDRFlagParamsSet[1];
		int extraOrder;
		for(int i=0; i<5; i++) {
			if (paramsSet[i] == 0)
				break;		// No more parameters.
			extraOrder = p->extraOrders[i];
            if (extraOrder < 1) {
                return EXPECT_POS_NUM;
            }
			orders.push_back(extraOrder);
		}
	} else {
        orders.push_back(2);
    }
    
    double pixelCombinationCutoff = 1.0;
    if (p->COMBFlagEncountered) {
        // Parameter: p->combinationSelection
        if (p->combinationSelection < 1.0)
            return EXPECT_POS_NUM;
        pixelCombinationCutoff = p->combinationSelection;
    }
    
    bool haveExternalCombinationDescription = false;
    ImagePtr externalCombinationDescription;
    if (p->CWAVFlagEncountered) {
        haveExternalCombinationDescription = true;
        // Parameter: p->pixelCombinations (test for NULL handle before using)
        if (p->pixelCombinations == nullptr)
            return NOWAV;
        externalCombinationDescription = CopyIgorDPWaveToMatrix(p->pixelCombinations);
        orders.resize(1);
        orders[0] = externalCombinationDescription->cols() / 2 - 1;
    }
    
    std::vector<double> pixelCombinationWeights;
    bool havePixelCombinationWeights = false;
    if (p->WGHTFlagEncountered) {
        // Parameter: p->pixelCombinationWeights (test for NULL handle before using)
        if (p->pixelCombinationWeights == NULL)
            return NOWAV;
        if ((WaveType(p->pixelCombinationWeights) & (NT_FP64 | NT_FP32)) == 0)
            return WAVE_TYPE_MISMATCH;
        int numDimensions;
        CountInt dimensionSizes[MAX_DIMENSIONS+1];
        err = MDGetWaveDimensions(p->pixelCombinationWeights, &numDimensions, dimensionSizes);
        if (err != 0)
            return err;
        if (numDimensions != 1)
            return INCOMPATIBLE_DIMENSIONING;
        if (dimensionSizes[ROWS] == 0)
            return INCOMPATIBLE_DIMENSIONING;
        
        pixelCombinationWeights = IgorWaveToVector(p->pixelCombinationWeights);
        havePixelCombinationWeights = true;
    }
    
    int nFramesToSkip = 0;
    int nFramesToInclude = -1;
    if (p->SUBFlagEncountered) {
		// Parameter: p->framesToSkip
        // Parameter: p->nFramesToInclude
        nFramesToSkip = p->framesToSkip + 0.5;
        nFramesToInclude = p->nFramesToInclude;
        if (nFramesToSkip < 0)
            return EXPECT_POS_NUM;
        if (nFramesToInclude < -1)
            return EXPECT_POS_NUM;
	}
    
    bool removeSaturatedPixels = false;
    if (p->NSATFlagEncountered) {
        removeSaturatedPixels = true;
		if (p->NSATFlagParamsSet[0]) {
			// Optional parameter: p->noSaturatedPixels
            removeSaturatedPixels = (p->noSaturatedPixels != 0);
		}
	}
    
    bool wantCrossCumulant = true;
    if (p->XCFlagEncountered) {
        if (p->XCFlagParamsSet[0]) {
            // Optional parameter: p->wantCrossCumulant
            wantCrossCumulant = (p->wantCrossCumulant != 0.0);
        }
    }
    
    bool wantAverageImage = false;
    if (p->AVGFlagEncountered) {
        wantAverageImage = true;
		if (p->AVGFlagParamsSet[0]) {
			// Optional parameter: p->doAverage
            wantAverageImage = (p->doAverage != 0);
		}
	}
    
    bool doPixelationCorrection = true;
    bool alsoCorrectVariance = true;
    if (p->PXCRFlagEncountered) {
		if (p->PXCRFlagParamsSet[0]) {
			// Optional parameter: p->doPixelationCorrection
            doPixelationCorrection = (p->doPixelationCorrection != 0.0);
            if (p->doPixelationCorrection == 2.0)
                alsoCorrectVariance = false;
		}
	}
    
    int batchSize = 0;
    if (p->BATFlagEncountered) {
		// Parameter: p->batchSize
        batchSize = p->batchSize + 0.5;
        if ((p->batchSize < 0.0) || (batchSize < 0)) {
            return EXPECT_POS_NUM;
        }
        if (batchSize > 0) {
            batchSize = std::max(10, batchSize);
        }
	}
    
    if (p->LAGFlagEncountered && p->LAGWFlagEncountered) {
        XOPNotice("Only one of the \"LAG\" or \"LAGW\" flags can be set\r");
        return PALM_ANALYSIS_XOP_ERROR;
    }
    
    std::vector<int> lagTimes;
    if (p->LAGFlagEncountered) {
        // Array-style optional parameter: p->lagTimes
        int* paramsSet = &p->LAGFlagParamsSet[0];
        int nLagTimesSpecified = 0;
        double d1;
        for(int i=0; i<100; i++) {
            if (paramsSet[i] == 0)
                break;		// No more parameters.
            d1 = p->lagTimes[i];
            int lagTime = static_cast<int>(d1);
            lagTimes.push_back(lagTime);
            nLagTimesSpecified += 1;
        }
    }
    if (p->LAGWFlagEncountered) {
        // Parameter: p->lagTimesWave (test for NULL handle before using)
        waveHndl lagTimesWave = p->lagTimesWave;
        if (lagTimesWave == NULL)
            return NOWAV;
        int waveType = WaveType(lagTimesWave);
        if ((waveType == TEXT_WAVE_TYPE) || (waveType == WAVE_TYPE) || (waveType == DATAFOLDER_TYPE) || (waveType & NT_CMPLX))
            return NT_INCOMPATIBLE;
        int nLagTimesInWave = WavePoints(lagTimesWave);
        std::unique_ptr<double[]> doubleBuffer(new double[nLagTimesInWave]);
        lagTimes.resize(nLagTimesInWave);
        err = MDGetDPDataFromNumericWave(lagTimesWave, doubleBuffer.get());
        if (err != 0)
            return err;
        for (int i = 0; i < nLagTimesInWave; ++i) {
            lagTimes[i] = doubleBuffer[i];
        }
        // drop any lag times that may be unnecessary
        lagTimes.resize(orders.at(0) - 1);
    }
    
    bool wantJackKnife = false;
    if (p->JACKFlagEncountered) {
        wantJackKnife = true;
        if (p->JACKFlagParamsSet[0]) {
            wantJackKnife = (p->doJackKnife != 0.0);
		}
	}
    
    bool wantDebugMessages = false;
	bool wantDebugPixelCombinationsWave = false;
    if (p->DEBGFlagEncountered) {
        wantDebugMessages = true;
        if (p->DEBGFlagParamsSet[0]) {
            // Optional parameter: p->printDebugInfo
            wantDebugMessages = (p->printDebugInfo != 0.0);
            wantDebugPixelCombinationsWave = (p->printDebugInfo == 2.0);
        }
    }
    
    SOFIOptions::AllowablePixelCombinations allowablePixelCombinations = SOFIOptions::NonOverlappingPixels;
    if (p->SAMEFlagEncountered) {
        allowablePixelCombinations = SOFIOptions::AllowOverlappingPixels;
        if (p->SAMEFlagParamsSet[0]) {
            // Optional parameter: p->allowOverlappingPixels
            if (p->allowOverlappingPixels == 0.0) {
                allowablePixelCombinations = SOFIOptions::NonOverlappingPixels;
            } else {
                allowablePixelCombinations = SOFIOptions::AllowOverlappingPixels;
            }
        }
    }
    
    bool useIgorFunctionForProgress;
    FUNCREF igorProgressReporterFunction;
    if (p->PROGFlagEncountered) {
        // Parameter: p->progStruct
        useIgorFunctionForProgress = true;
        igorProgressReporterFunction = p->progStruct->funcRef;
    } else {
        useIgorFunctionForProgress = false;
    }
    
    bool quiet = false;
    if (p->QFlagEncountered) {
        quiet = true;
    } else {
        if (!RunningInMainThread())
            quiet = true;	// no progress reporting if running in an Igor-preemptive thread
    }
    
    DataFolderAndName outputWaveParams;
    if (p->DESTFlagEncountered) {
        // Parameter: p->dest
        if ((orders.size() > 1) && (strlen(p->dest.name) > MAX_OBJ_NAME - 1))   // must have room to append numeric suffic if multiple orders
            return NAME_TOO_LONG;
        if (wantAverageImage && (strlen(p->dest.name) > MAX_OBJ_NAME - 4))      // room for "_avg" suffix
            return NAME_TOO_LONG;
        if (wantJackKnife && (strlen(p->dest.name) > MAX_OBJ_NAME - 6))         // room for "_jack<digit>" suffix
            return NAME_TOO_LONG;
        outputWaveParams = p->dest;
    } else {
        outputWaveParams.dfH = NULL;
        strcpy(outputWaveParams.name, "M_SOFI");
    }
    
	// Main parameters.
    
	if (p->inputFilePathEncountered) {
		// Parameter: p->inputFilePath (test for NULL handle before using)
        if (p->inputFilePath == NULL)
            return EXPECTED_STRING_EXPR;
	} else {
        return EXPECTED_STRING_EXPR;
    }
    
    try {
        SOFIOptions sofiOptions;
        sofiOptions.orders = orders;
        sofiOptions.wantCrossCumulant = wantCrossCumulant;
        sofiOptions.allowablePixelCombinations = allowablePixelCombinations;
        sofiOptions.lagTimes = lagTimes;
        sofiOptions.nFramesToSkip = nFramesToSkip;
        sofiOptions.nFramesToInclude = nFramesToInclude;
        sofiOptions.batchSize = batchSize;
        sofiOptions.doPixelationCorrection = doPixelationCorrection;
        sofiOptions.alsoCorrectVariance = alsoCorrectVariance;
        sofiOptions.wantAverageImage = wantAverageImage;
        sofiOptions.wantJackKnife = wantJackKnife;
        if (haveExternalCombinationDescription) {
            sofiOptions.haveExternalPixelCombination = true;
            sofiOptions.externalPixelCombinations = *externalCombinationDescription;
        }
        sofiOptions.pixelCombinationCutoff = pixelCombinationCutoff;
        sofiOptions.pixelCombinationWeights = pixelCombinationWeights;
        sofiOptions.wantDebugMessages = wantDebugMessages;
        sofiOptions.wantDebugPixelCombinationsWave = wantDebugPixelCombinationsWave;
        sofiOptions.jackKnifeAllocator = [&](int nRows, int nCols, int nImages, double offset, double deltaX, double deltaY, int order, int orderIndex, bool multipleOrders) {
            size_t nPixelsRequired = (size_t)nRows * (size_t)nCols * (size_t)nImages;
            DataFolderAndName jackKnifeOutputWaveParams = outputWaveParams;
            strcat(jackKnifeOutputWaveParams.name, "_jack");
            if (multipleOrders) {
                int nameLength = strlen(jackKnifeOutputWaveParams.name);
                jackKnifeOutputWaveParams.name[nameLength] = static_cast<char>(order + 48);
                jackKnifeOutputWaveParams.name[nameLength + 1] = '\0';
            }
            waveHndl jackknifeWave;
            CountInt dimensionSizes[MAX_DIMENSIONS + 1];
            dimensionSizes[0] = nRows;
            dimensionSizes[1] = nCols;
            dimensionSizes[2] = nImages;
            dimensionSizes[3] = 0;
            int err = MDMakeWave(&jackknifeWave, jackKnifeOutputWaveParams.name, jackKnifeOutputWaveParams.dfH, dimensionSizes, NT_FP64, 1);
            if (err != 0)
                throw err;
            MDSetWaveScaling(jackknifeWave, ROWS, &deltaX, &offset);
            MDSetWaveScaling(jackknifeWave, COLUMNS, &deltaY, &offset);
            memset(WaveData(jackknifeWave), 0, nPixelsRequired * sizeof(double));
            return ExternalImageBuffer(reinterpret_cast<double*>(WaveData(jackknifeWave)), nPixelsRequired);
        };
        
        // access the data to load
        std::string inputFilePath = ConvertHandleToString(p->inputFilePath);
        std::shared_ptr<ImageLoader> imageLoader = GetImageLoader(inputFilePath, cameraType);
        std::shared_ptr<ImageLoader> imageLoaderWrapper(new ImageLoaderWrapper(imageLoader));
        dynamic_cast<ImageLoaderWrapper*>(imageLoaderWrapper.get())->setImageRange(sofiOptions.nFramesToSkip, sofiOptions.nFramesToInclude);
        std::shared_ptr<ImageLoader> threadedImageLoaderWrapper(new BackgroundThreadImageLoaderWrapper(imageLoaderWrapper));

        std::shared_ptr<ProgressReporter> progressReporter;
        if (quiet == 1) {
            progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_Silent);
        } else {
            if (useIgorFunctionForProgress != 0) {
                progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_IgorUserFunction(igorProgressReporterFunction));
            } else {
                progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_IgorCommandLine);
            }
        }
        
        // do the actual calculation
        std::vector<ImagePtr> sofiOutputImages;
        DoNewSOFI(threadedImageLoaderWrapper, sofiOptions, progressReporter, sofiOutputImages);
        for (size_t i = 0; i < orders.size(); ++i) {
            DataFolderAndName adjustedOutputParams = outputWaveParams;
            if (orders.size() > 1) {
                int nameLength = strlen(adjustedOutputParams.name);
                adjustedOutputParams.name[nameLength] = static_cast<char>(orders[i] + 48);
                adjustedOutputParams.name[nameLength + 1] = '\0';
            }
            waveHndl outputWave = CopyMatrixToIgorDPWave(sofiOutputImages.at(i), adjustedOutputParams);
            double offset = sofiOptions.outputUsedBoundaryMargin;
            double deltaX = (imageLoader->getXSize() - 2.0 * offset) / (double)sofiOutputImages.at(i)->rows();
            double deltaY = (imageLoader->getYSize() - 2.0 * offset) / (double)sofiOutputImages.at(i)->cols();
            MDSetWaveScaling(outputWave, ROWS, &deltaX, &offset);
            MDSetWaveScaling(outputWave, COLUMNS, &deltaY, &offset);
        }
        if (wantAverageImage) {
            DataFolderAndName averageOutputWaveParams = outputWaveParams;
            strcat(averageOutputWaveParams.name, "_avg");
            CopyMatrixToIgorDPWave(sofiOptions.averageImage, averageOutputWaveParams);
        }
        // verify the provided jackknife storage had the right size
        if (wantJackKnife) {
            for (size_t i = 0; i < orders.size(); ++i) {
                if (!sofiOptions.jackKnifeImages.at(i).bufferIsFull()) {
                    throw std::runtime_error("did not fill entire jackknife buffer");
                }
            }
        }
    }
    catch (int e) {
        return e;
    }
    catch (std::bad_alloc e) {
        return NOMEM;
    }
    catch (std::runtime_error e) {
        XOPNotice(e.what());
        XOPNotice("\r");
        return PALM_ANALYSIS_XOP_ERROR;
    }
    catch (std::logic_error e) {
        XOPNotice(e.what());
        XOPNotice("\r");
        return PALM_ANALYSIS_XOP_ERROR;
    }
    catch (...) {
        XOPNotice("An unknown error occurred\r");
        return WM_UNKNOWN_ERROR;
    }
    
	return err;
}

static int ExecuteSOFIPixelCombinations(SOFIPixelCombinationsRuntimeParamsPtr p)
{
    // Flag parameters.
    
    double pixelCombinationCutoff = 10.0;
    if (p->COMBFlagEncountered) {
        // Parameter: p->combinationSelection
        if (p->combinationSelection < 1.0)
            return EXPECT_POS_NUM;
        pixelCombinationCutoff = p->combinationSelection;
    }
    
    SOFIOptions::AllowablePixelCombinations allowablePixelCombinations = SOFIOptions::NonOverlappingPixels;
    if (p->SAMEFlagEncountered) {
        allowablePixelCombinations = SOFIOptions::AllowOverlappingPixels;
        if (p->SAMEFlagParamsSet[0]) {
            // Optional parameter: p->allowOverlappingPixels
            if (p->allowOverlappingPixels == 0.0) {
                allowablePixelCombinations = SOFIOptions::NonOverlappingPixels;
            } else {
                allowablePixelCombinations = SOFIOptions::AllowOverlappingPixels;
            }
        }
    }
    
    // Main parameters.
    
    int order = 2;
    if (p->orderEncountered) {
        // Parameter: p->order
        order = p->order + 0.5;
        if (!Within(order, kMinSofiOrder, kMaxSofiOrder))
            return EXPECT_POS_NUM;
    } else {
        return EXPECT_POS_NUM;
    }
    
    waveHndl output = CopyMatrixToIgorDPWave(PixelCombinationsForOrderAsMatrix(order, allowablePixelCombinations, pixelCombinationCutoff), "M_SOFIPixelCombinations");
    
    return ((output == NULL) ? NOMEM : 0);
}

static int ExecuteWriteCCDImages(WriteCCDImagesRuntimeParamsPtr p) {
	int err = 0;

	RecycledMatrixCleaner recycledMatrixCleaner;
    
	// Flag parameters.
    
    bool compress = false;
	if (p->CMPRFlagEncountered) {
        compress = true;
		if (p->CMPRFlagParamsSet[0]) {
			// Optional parameter: p->compress
            compress = (p->compress != 0.0);
		}
	}
    
    int nFramesToSkip = 0;
	if (p->SFlagEncountered) {
		// Parameter: p->framesToSkip
        nFramesToSkip = p->framesToSkip + 0.5;
        if (nFramesToSkip < 0)
            return EXPECT_POS_NUM;
	}
    
    int nFramesToInclude = -1;
	if (p->CFlagEncountered) {
		// Parameter: p->framesToInclude
        nFramesToInclude = p->framesToInclude + 0.5;
	}
    
    bool overwrite = false;
    if (p->OFlagEncountered) {
        overwrite = true;
		if (p->OFlagParamsSet[0]) {
			// Optional parameter: p->overwrite
            overwrite = (p->overwrite != 0.0);
		}
	}
    
    bool quiet = false;
	if (p->QFlagEncountered) {
        quiet = true;
	} else {
        if (!RunningInMainThread())
            quiet = true;	// no progress reporting if running in an Igor-preemptive thread
    }
    
    bool useIgorFunctionForProgress;
    FUNCREF igorProgressReporterFunction;
    if (p->PROGFlagEncountered) {
        // Parameter: p->progStruct
        useIgorFunctionForProgress = true;
        igorProgressReporterFunction = p->progStruct->funcRef;
    } else {
        useIgorFunctionForProgress = false;
    }
    
	// Main parameters.
    waveHndl imageWave = NULL;
	if (p->imagesWaveEncountered) {
		// Parameter: p->imagesWave (test for NULL handle before using)
        imageWave = p->imagesWave;
        if (imageWave == NULL)
            return NOWAV;
	} else {
        return NOWAV;
    }
    
    std::string outputPath;
    bool haveOutputPath = false;
	if (p->outputPathEncountered) {
		if (p->outputPathParamsSet[0]) {
			// Optional parameter: p->outputPath (test for NULL handle before using)
            if (p->outputPath == NULL) {
                return EXPECTED_STRING;
            }
            outputPath = ConvertHandleToString(p->outputPath);
            haveOutputPath = true;
		}
	}
    
    try {
        if (!haveOutputPath) {
            // prompt user for a location to save the data
            char outputPathBuf[MAX_PATH_LEN + 1];
            outputPathBuf[0] = 0;
#ifdef MACIGOR
            const char *fileFilterStr = "TIFF File::.tif;";
#else
            const char *fileFilterStr = "TIFF File (*.tif)\0*.tif\0\0";
#endif
            err = XOPSaveFileDialog("Choose file location", fileFilterStr, NULL, "", "", outputPathBuf);
            if (err)
                return err;
            if (strlen(outputPathBuf) == 0)
                return 0;   // user cancelled the dialog
            outputPath = outputPathBuf;
            overwrite = true;   // the dialog will have asked the user if they want to overwrite
        }
        outputPath = ConvertPathToNativePath(outputPath);
        
        std::shared_ptr<ImageLoader> imageLoader(new ImageLoaderIgor(imageWave));
        std::shared_ptr<ImageOutputWriter> outputWriter(new LocalizerTIFFImageOutputWriter(outputPath, overwrite, compress, imageLoader->getStorageType()));
        std::shared_ptr<ProgressReporter> progressReporter;
        if (quiet == 1) {
            progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_Silent);
        } else {
            if (useIgorFunctionForProgress != 0) {
                progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_IgorUserFunction(igorProgressReporterFunction));
            } else {
                progressReporter = std::shared_ptr<ProgressReporter> (new ProgressReporter_IgorCommandLine);
            }
        }
        WriteImagesToDisk(imageLoader, outputWriter, progressReporter, nFramesToSkip, nFramesToInclude);
    }
    catch (...) {
        
    }
    
	return err;
}

